package br.com.ifspcjo.ads.web2.model;

public class Model
{
    private String email;
    private String senha;

    public void setEmail(String email) 
    {
        this.email = email;
    }
    
    public String getEmail() 
    {
        return email;
    }

    public void setSenha(String senha) 
    {
        this.senha = senha;
    }

    public String getSenha() 
    {
        return senha;
    }
}
